#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	unsigned char	uc;

	uc = (unsigned char) c;
	while (*str)
	{
		if (uc == *str)
			return ((char *) str);
		str++;
	}
	if (uc == '\0')
		return ((char *) str);
	return (NULL);
}
