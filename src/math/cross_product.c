#include "rt_driver.h"

t_v4f	cross_product(t_v4f a, t_v4f b)
{
	t_v4f	result;

	result = (t_v4f){a[1] * b[2] - a[2] * b[1], a[2] * b[0] - a[0] * b[2], a[0]
		* b[1] - a[1] * b[0], 1.0f};
	return (result);
}
